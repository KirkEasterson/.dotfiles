return {
	'goolord/alpha-nvim',
	dependencies = {
		'nvim-tree/nvim-web-devicons',
		'BlakeJC94/alpha-nvim-fortune',
	},
	lazy = false,
	priority = 999, -- 1 less than gruvbox
	config = function()
		local alpha = require("alpha")
		local dashboard = require("alpha.themes.dashboard")

		-- Set header
		dashboard.section.header.val = month_arts[os.date("*t").month]

		-- Set menu
		dashboard.section.buttons.val = {
			dashboard.button("e", "  > New file", ":ene <BAR> startinsert <CR>"),
			dashboard.button("f", "󰈞  > Find file", ":Telescope find_files<CR>"),
			dashboard.button("o", "  > Recent", ":Telescope oldfiles<CR>"),
			dashboard.button("r", "󰦛  > Restore session", ":RestoreSession<CR>"),
			dashboard.button("t", "  > File tree", ":NvimTreeOpen<CR>"),
			dashboard.button("q", "󰅚  > Quit NVIM", ":qa<CR>"),
		}

		local fortune = require("alpha.fortune")
		dashboard.section.footer.val = fortune()

		-- Send config to alpha
		alpha.setup(dashboard.opts)

		vim.api.nvim_create_autocmd({ "FileType" }, {
			pattern = { 'alpha', },
			callback = function()
				vim.opt_local.foldenable = false
			end,
			desc = "Disable folding in Alpha",
		})

		vim.api.nvim_create_autocmd("User", {
			pattern = "AlphaReady",
			callback = function()
				vim.opt.cmdheight = 0
				vim.opt.showtabline = 0
				vim.opt.laststatus = 0

				vim.api.nvim_create_autocmd(
					"BufUnload",
					{
						pattern = "<buffer>",
						callback = function()
							vim.opt.cmdheight = 1
							vim.opt.showtabline = 2
							vim.opt.laststatus = 3
						end,
					}
				)
			end,
			desc = "Disable Bufferline And Lualine in Alpha",
		})
	end,
}
